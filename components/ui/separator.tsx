import * as React from "react";
import { cn } from "@/lib/utils";

/**
 * A simple horizontal or vertical visual separator.
 * Useful for grouping sections or elements in the UI.
 */
function Separator({
  className,
  orientation = "horizontal",
  ...props
}: React.HTMLAttributes<HTMLDivElement> & {
  orientation?: "horizontal" | "vertical";
}) {
  return (
    <div
      className={cn(
        "shrink-0 bg-gray-200",
        orientation === "horizontal"
          ? "h-px w-full"
          : "h-full w-px",
        className
      )}
      {...props}
    />
  );
}

export { Separator };
